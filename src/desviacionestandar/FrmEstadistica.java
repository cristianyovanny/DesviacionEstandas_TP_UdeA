    public void mostrar(){
        //Crear objeto para listar datos
        DefaultListModel dlm = new DefaultListModel();
        
        //Recorrer la lista de valores
        for(int i = 0; i < totalDatos; i++){
            //Agregar el elemento lista
            dlm.addElement(datos[i]);
        }
        
        //Asignar el objeto para listar
        lstDatos.setModel(dlm);
    }
    //Subprograma para calcular sumatoria
    public double sumatoria(){
        double s = 0;
        for(int i = 0; i < totalDatos; i++){
            s = s + datos[i];
        }
        return s;
    }
    
    //Subprograma para calcular promedio
    public double promedio(){
        double p = 0;
        if(totalDatos > 0){
            p = sumatoria() / totalDatos;
        }
        return p;
    }
    
    //Subprograma para calcular desviación
    double Desviacion(){
        double d = 0;
        if (totalDatos > 1) {
            double p = promedio();
            double s = 0;
            for(int i = 0; i < totalDatos; i++){
                s = s + Math.abs(datos[i] - p);
            }
            d = s / (totalDatos - 1);
        }
        return d;
    }
    
