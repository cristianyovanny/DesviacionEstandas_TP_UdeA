    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try{
            // Verificar que se puedan agregar más datos
            if(totalDatos < datos.length){
                //Obtener el dato a agregar
                double dato = Double.parseDouble(txtDato.getText());
                
                //Incrementar el total de datos agregados
                totalDatos++;
                
                //Agregar el dato final
                datos[totalDatos - 1] = dato;
                
                //Actualizar los datos mostrados
                mostrar();
                                               
            } else {
                JOptionPane.showMessageDialog(null, "No se puede agregar datos");
            }
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Error en el dato digitado:\n" + ex);
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        if(totalDatos > 0 && lstDatos.getSelectedIndex() >= 0) {
            //Recorrer desde la posición seleccionada hasta la penultima
            for(int i = lstDatos.getSelectedIndex(); i < totalDatos - 1; i++) {
                datos[i] = datos[i + 1];
            }
            //Disminuir el tamaño del vector
            totalDatos--;
            
            //Actualizar los datos mostrados
            mostrar();
        } else {
            JOptionPane.showMessageDialog(null, "No haay datos seleccionado");
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

